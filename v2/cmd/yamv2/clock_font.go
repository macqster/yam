func renderBreamDecoClock(text string) string {
	rows := make([]string, 7)
	for _, ch := range text {
		glyph, ok := breamDecoFont[ch]
		if !ok {
			continue
		}
		for i := 0; i < 7; i++ {
			rows[i] += glyph[i] + " "
		}
	}
	return strings.Join(rows, "\n")
}
